// Java Program to Swap Two Numbers
public class a5{
    
    public static void main(String args[]){
        int a = 6;
        int b = 8;
        int temp;
        temp = a;
        a = b;
        b = temp;
    
        System.out.println("First number = " + a);
        System.out.println("Second number = " + b);
    }
}